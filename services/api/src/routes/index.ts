import { FastifyInstance } from 'fastify';
import { authRoutes } from './auth';
import { usersRoutes } from './users';
import { remindersRoutes } from './reminders';
import { listsRoutes } from './lists';
import { devicesRoutes } from './devices';
import { subscriptionsRoutes } from './subscriptions';
import { webhooksRoutes } from './webhooks';
import { integrationsCalendarRoutes } from './integrations/calendar';
import { adminRoutes } from './admin';
import { countriesRoutes } from './countries';
import { familyRoutes } from './family';
import { teamRoutes } from './team';
import { translationsRoutes } from './translations';
import { categoriesRoutes } from './categories';
import { professionsRoutes } from './professions';
import { familyAlarmsRoutes } from './family-alarms';
import { menuConfigPublicRoutes } from './menu-config';
import { fridgeItemsRoutes } from './fridge-items';

export async function registerRoutes(server: FastifyInstance) {
  await server.register(authRoutes,         { prefix: '/v1/auth' });
  await server.register(usersRoutes,        { prefix: '/v1/users' });
  await server.register(remindersRoutes,    { prefix: '/v1/reminders' });
  await server.register(listsRoutes,        { prefix: '/v1/lists' });
  await server.register(devicesRoutes,      { prefix: '/v1/devices' });
  await server.register(subscriptionsRoutes, { prefix: '/v1/subscriptions' });
  await server.register(webhooksRoutes,     { prefix: '/v1/webhooks' });
  await server.register(integrationsCalendarRoutes, { prefix: '/v1/integrations/calendar' });
  await server.register(adminRoutes,        { prefix: '/v1/admin' });
  await server.register(countriesRoutes,    { prefix: '/v1/countries' });
  await server.register(familyRoutes,       { prefix: '/v1/family' });
  await server.register(teamRoutes,         { prefix: '/v1/team' });
  await server.register(translationsRoutes, { prefix: '/v1/admin/translations' });
  await server.register(categoriesRoutes,   { prefix: '/v1/categories' });
  await server.register(professionsRoutes,  { prefix: '/v1/professions' });
  await server.register(familyAlarmsRoutes,      { prefix: '/v1/family-alarms' });
  await server.register(fridgeItemsRoutes,       { prefix: '/v1/fridge-items' });
  await server.register(menuConfigPublicRoutes,  { prefix: '/v1' });
}
